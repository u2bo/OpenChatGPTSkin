export type StudioCopy = typeof copy["zh-CN"];

export function studioCopy(locale: StudioLocale): StudioCopy {
  return copy[locale] as StudioCopy;
}

export function localizedTheme(
  theme: StudioThemeListItem,
  locale: StudioLocale,
): { readonly name: string; readonly description?: string } {
  const localized = theme.localized?.[locale];
  const description = localized?.description ?? theme.description;
  return {
    name: localized?.name ?? theme.name,
    ...(description ? { description } : {}),
  };
}
